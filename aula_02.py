print(123) # Função print é usada para exiber informações na tela
print(12, 34) # A vírgula concatena valores adicionando um separador entre eles, por padrão é um espaço
print(12, 34, sep='-') # O argumento "sep" modifica o separador que você quer usar para concatenar com a vírgula
print(123, end='45\n') # O argumento "end" modifica o final do print, por padrão é uma quedra de linha "\n"