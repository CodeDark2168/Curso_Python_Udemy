# Utilize aspas duplas ou simples para o python entender que se trata de uma string
print('string')
print("string")

# A barra invertida é um caracter de escape que anula a função do próximo caracter
print("string \"")

# O "r" exibe o carcter de escape
print(r"string \"")

# Para exibir as aspas também pode se utilizar aspas de outro tipo
print("'string'")
print('"string"')