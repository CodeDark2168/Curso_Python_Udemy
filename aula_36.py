# Operadores de atribuição (= += -= *= /= //= **= %=)

contador = 0

contador += 1
print(contador)

###

contador = 'a'

contador += 'b'
print(contador)

###

contador = 5

contador -= 1
print(contador)

###

contador = 1

contador *= 5
print(contador)

###

contador = 'a'

contador *= 10
print(contador)

###

contador = 10

contador /= 2
print(contador)

###

contador = 10

contador //= 3
print(contador)

###

contador = 2

contador **= 3
print(contador)

###

contador = 10

contador %= 3
print(contador)